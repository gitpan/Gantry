#-----------------------------------------------------------------
sub init {
    my ( $self, $r ) = @_;

    # process SUPER's init code
    $self->SUPER::init( $r );

    $self->set_dbuser( $self->fish_config( 'dbuser' ) || '' );
    $self->set_auth_dbuser( $self->fish_config( 'auth_dbuser' ) || '' );
} # END init


