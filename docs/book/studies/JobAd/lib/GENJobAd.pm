#-----------------------------------------------------------------
# $self->do_main( )
#-----------------------------------------------------------------
sub do_main {
    my ( $self ) = shift;

    $self->stash->view->template( 'main.tt' );
    $self->stash->view->title( 'JobAd' );

    $self->stash->view->data( {
        pages => [
            { link => 'job', label => 'Job' },
            { link => 'skill', label => 'Skill' },
            { link => 'position', label => 'Position' },
        ],
    } );
}

